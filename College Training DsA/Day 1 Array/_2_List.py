#Slicing
arr=[11,22,33,44,55,66,77]
print(arr[4])
print(arr[-1])
print(arr[1:5])
print(arr[4:6])
print(arr[:6])
print(arr[4:])
print(arr[:]) 
print(arr[::2])       # so here 2 means it will print value after leaving one value
print(arr[::3])
print(arr[::-1])      #used to print reverse array
print(arr[::-2])